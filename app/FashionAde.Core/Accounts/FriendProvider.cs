using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using SharpArch.Core.DomainModel;

namespace FashionAde.Core.Accounts
{
    public class FriendProvider : Entity
    {
        private string name;
        private string imageUri;

        public virtual string Name
        {
            get { return name; }
            set { name = value; }
        }

        public virtual string ImageUri
        {
            get { return imageUri; }
            set { imageUri = value; }
        }

        public FriendProvider() {}
        public FriendProvider(int id)
        {
            this.Id = id;
        }
    }
}
