using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using FashionAde.Core.OutfitEngine;
using FashionAde.Core.Clothing;
using SharpArch.Core.DomainModel;

namespace FashionAde.Core.Trends
{
    public class Trend : Entity
    {
        private string name;
        private IList<string> keywords = new List<string>();

        public virtual string Name
        {
            get { return name; }
            set { name = value; }
        }

        public virtual IList<string> Keywords
        {
            get { return keywords; }
            set { keywords = value; }
        }
    }
}
