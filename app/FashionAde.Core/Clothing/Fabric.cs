using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace FashionAde.Core.Clothing
{
    public class Fabric : Tag
    {
        public Fabric() {}

        public Fabric(int id)
        {
            this.Id = id;
        }

        public Fabric(int id, string description)
        {
            this.Id = id;
            this.Description = description;
        }

        public virtual bool Shines { get; set; }
    }
}
